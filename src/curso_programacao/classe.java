package curso_programacao;

public class classe {
		
	
		String name;
		String email;
		int quarto;
		
		public  classe (String name, String email, int quarto) {
			this.name = name;
			this.email = email;
			this.quarto = quarto;
		}

		public String getName() {
			
			return name;
		}
		public String getEmail() {
			
			return email;
		}
		public int getQuarto() {
			
			return quarto;
		}
		

		
		
}

